//Ciclo while 
//Imprimir los valores del 0 al 5;
let contador = 5, repeticiones = 0;

while(contador >= repeticiones){
    console.log(`${contador--}`);
    //contador++;
}


//Ciclo do while, (Se ejecuta al menos una vez el bloque de codigo)
contador = 5;
do{
    console.log(contador--)
}while(contador >= repeticiones);